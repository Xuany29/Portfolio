    <h1>How to make your own herbarium?</h1>
    <h2>What is herbarium?</h2>
    <p>A herbarium (plural: herbaria) is a collection of preserved plant specimens and associated data used for scientific study.</p>
    <p>The specimens may be whole plants or plant parts; these will usually be in dried form mounted on a sheet of paper (called exsiccatum, plur. exsiccata) but, depending upon the material, may also be stored in boxes or kept in alcohol or other preservative.The specimens in a herbarium are often used as reference material in describing plant taxa; some specimens may be types, some may be specimens distributed in series called exsiccatae.</p>
    <p>The same term is often used in mycology to describe an equivalent collection of preserved fungi, otherwise known as a fungarium.A xylarium is a herbarium specialising in specimens of wood.The term hortorium (as in the Liberty Hyde Bailey Hortorium) has occasionally been applied to a herbarium specialising in preserving material of horticultural origin.</p>
    <p>There are two parts to make herbarium specimens:<strong>Pressing and Mounting</strong></p>
    </div>

