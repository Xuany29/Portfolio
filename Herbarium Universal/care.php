      <h1>How to Preserve Herbarium Specimen?</h1>
        <h2>There are some ways to protect your herbarium specimen :</h2>
        <ul>
          <li>The best temperature for storage is from 50–65ºF</li>
          <li>It is very important to keep loose material safe and secure. Folding a capsule correctly is easy : fold the bottom flap up first, then the two shorter sides and finally the top flap.</li>
          <li>Always carry specimens on a piece of cardboard : these can be found throughout the Herbarium.</li>
          <li>In boxes, specimens should be stored so that they are as level as possible. Notice the foam on the right hand side which prevents them from moving.</li>
          <li>Cabinet doors should always be closed when they are not in use. As well as preventing the spread of herbarium beetles the cabinets are fireproof.</li>
          <li>Using a flat surface to view specimens helps prevent damage to the material. Use both hands to move the specimens keeping them horizontal at all times.</li>
          <li>Protect specimens from light : even electrical light can cause damage and decay.</li>
        </ul>
    </div>

    <br><br>
    <div class="text-care">
        <h2>And these are the ways will damage herbarium specimen :</h2>
        <ul>
          <li>A badly folded capsule is easily damaged and the contents can fall out. Avoid using paper clips, but if it is necessary use an archival one.</li>
          <li>Open doors let pests in.</li>
          <li>Tilting specimens or shuffling through them can cause damage.</li>
          <li>Tightly folded and overcrowded genus covers crush specimens.</li>
          <li>Unsupported specimens get damaged!</li>
          <li>Specimens need to be covered up when not in use. Even electrical light causes daicay.</li>
        </ul>
    </div>
  

